def soma(num1: int, num2: int) -> int:
    resultado = num1 + num2
    print(f'O resultado é {resultado}')


def subtracao(num1: int, num2: int) -> int:
    resultado = num1 - num2
    print(f'O resultado é {resultado}')


def multiplicacao(num1: int, num2: int) -> int:
    resultado = num1 * num2
    print(f'O resultado é {resultado}')


def divisao(num1: int, num2: int) -> int:
    resultado = num1 / num2
    print(f'O resultado é {resultado}')
